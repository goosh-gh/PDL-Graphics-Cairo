sub new {
    my ($class, $name) = @_;
    $name //= 'viridis';
    die "Unknown colormap: $name" unless exists $MAPS{$name};
    return bless { name => $name, pts => $MAPS{$name} }, $class;
}

sub list { return sort keys %MAPS }

sub rgb_at {
    my ($self, $v) = @_;
    $v = max(0, min(1, $v));

    my $pts = $self->{pts};

    #

    return @{$pts->[0]}[1..3]  if $v <= $pts->[0][0];
    return @{$pts->[-1]}[1..3] if $v >= $pts->[-1][0];

    #

    for my $i (1 .. $#$pts) {
        my ($t0, $r0, $g0, $b0) = @{ $pts->[$i-1] };
        my ($t1, $r1, $g1, $b1) = @{ $pts->[$i]   };
        if ($v <= $t1) {
            my $f = ($v - $t0) / ($t1 - $t0);
            return ($r0 + $f*($r1-$r0),
                    $g0 + $f*($g1-$g0),
                    $b0 + $f*($b1-$b0));
        }
    }
    return @{$pts->[-1]}[1..3];
}

#  rgb_at 
sub rgb_norm {
    my ($self, $val, $vmin, $vmax) = @_;
    my $v = ($vmax == $vmin) ? 0.5 : ($val - $vmin) / ($vmax - $vmin);
    return $self->rgb_at($v);
}

# 256  RGB 
sub lut {
    my ($self, $n) = @_;
    $n //= 256;
    return [map { [$self->rgb_at($_ / ($n-1))] } 0..$n-1];
}

